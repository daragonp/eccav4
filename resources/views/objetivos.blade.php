        <center><h1>Objetivo</h1></center>
        <hr>
            <p><strong><em>E.C.A</em></strong>, Tienen como objetivo principal vivir la 
            plenitud humana ofertada por Cristo al modo Afro. Y a partir de ahí, cumplir 
            con las tareas de desarrollo integral necesarias en la comunidad Afro.</p>
            <p>Una de las razones por las que se inicia la Emisora <strong><em>E.C.A</em></strong>
            es que, al observar los ministerios cristianos en las regiones de población Afro, 
            la mayoría de ellos son dirigidos y controlados por pastores y líderes cristianos 
            mestizos, cuya misión se fundamenta en la llamada Teología Misionera, que tienen 
            dos manifestaciones:</p>
            <p>La primera, como Teología de conversión de los infieles paganos de los siglos 
                XV-XX, dando la visión de un Dios arrepentido de haber maldecido a los hijos 
                de Cam y que ha abierto un espacio en un rincón del cielo y ha abierto la puerta 
                trasera para que entren los paganos.</p>
            <p>La segunda, como Teología de la plantación de iglesias satélites, con la visión 
                de colonizar a los pobres paganos, atrasados, perdidos e indigentes que cargan 
                sobre sus hombros la supuesta maldición de Noé.</p>
            <p>Esta teología de plantación de iglesias ha traído consigo que lleguen personas 
                foráneas a establecer negocio y explotar los recursos, desplazando a los 
                miembros de la comunidad afro. Y al igual que la riqueza que saquean, los diezmos, 
                ofrendas no se reinvierte en la comunidad, sino que son extraída trayendo el 
                empobrecimiento.</p>
            <p>Son ministerio que tienen su oficina principal fuera de la comunidad afro, que no ven 
                la necesidad de formar líderes cristianos dentro de la comunidad Afro.</p>
            <p>La evangelización se trata de un asedio misionero dirigido con un espíritu de conquista, 
                Y los pocos líderes y pastores afros, aprenden un evangelio universal, que fomenta 
                la pérdida de identidad, porque ellos mismos no la tienen.</p>
            <p>Los pastores afros, se limitan con buena intensión a enseñar la comunión con Dios, 
                pero sin los otros dos elementos, la aceptación de sí mismos y la aceptación de pueblo.</p>
            <p>Esto equivales a enseñar solo el primer y gran mandamiento <em>Mat 22:37 … </em>
            <em>Amarás al Señor tu Dios con todo tu corazón, y con toda tu alma, y con toda 
                tu mente…</em>E ignorar el segundo<em> Mateo 22:39 Y el segundo es semejante:&nbsp; 
                    Amarás a tu prójimo como a ti mismo.</em></p>
            <p>Es decir, dicho de otra manera, la identidad lleva a amarse a sí mismo, para amar 
                al prójimo.</p>
            <p>No sobra aclarar, que el mensaje del evangelio de verdad de nuestro Señor 
                Jesucristo en para toda la humanidad, puesto que Dios Padre muestra su amor 
                para la humanidad al enviar a su Hijo Jesús para salvar el mundo, como dice 
                el Apóstol Juan en <em>Juan 3:16 Porque de tal manera amó Dios al mundo, que 
                    ha dado a su Hijo unigénito, para que todo aquel que en él cree, no se 
                    pierda, mas tenga vida eterna.</em></p>
            <p>Por lo tanto, es claro que el señor no hace acepción de personas, 
                como dice Moisés en <em>Deuteronomio 10:17 Porque Jehová vuestro Dios 
                    es Dios de dioses y Señor de señores, Dios grande, poderoso y temible, 
                    que no hace acepción de personas, ni toma cohecho; ….</em></p>
            <p>Pero lo anterior no contradice que Jehová tenga enviados para grupos poblacionales 
                específicos, como dijo el apóstol Pablo a la iglesia de Galacia en la epístola,
                 leemos en <em>Gálatas 2:7 Antes, por el contrario, como vieron que me había sido 
                     encomendado el evangelio de la incircuncisión, como a Pedro el de la 
                     circuncisión (pues el que actuó en Pedro para el apostolado de la 
                     circuncisión, actuó también en mí para con los gentiles),</em></p>
            <p>Aquí en este pasaje vemos que el Señor, envió mensajeros para dos grupos 
                poblacionales que tenían particularidades.</p>
            <p>Para el apóstol Pablo en un principio fue una locura el mensaje que 
                Ananías le entrego del Señor, acerca de llevar la palabra a los gentiles, 
                como leemos en <em>Hechos 9:15 El Señor le dijo: Ve, porque instrumento 
                    escogido me es éste, para llevar mi nombre en presencia de los gentiles, 
                    y de reyes, y de los hijos de Israel;</em></p>
            <p>En la frase “<em>y de los hijos de Israel</em>” el Señor le dijo que, 
            si se le llegaba un Israelita, debería anunciarle el evangelio, pero su prioridad 
            eran los gentiles.</p>
            <p>Pablo insistió en ir a las comunidades Israelitas de la provincia Romana de Asía, 
                de tal manera a que el E.S. le prohibió predicar en Asía como leemos en <em>
                    Hechos 16:6 Y atravesando Frigia y la provincia de Galacia, les fue 
                    prohibido por el Espíritu Santo hablar la palabra en Asia; 7 y cuando 
                    llegaron a Misia, intentaron ir a Bitinia, pero el Espíritu no se lo
                     permitió.</em></p>
            <p>Sería fuera de contexto decir que el Señor hizo acepción de personas con los 
                habitantes de la provincia romana de asía, él Señor simplemente presionó a 
                Pablo para que fuera a los Gentiles de Europa, como leemos en <em>Hechos 
                    16:9 Y se le mostró a Pablo una visión de noche:&nbsp; un varón macedonio 
                    estaba en pie, rogándole y diciendo: Pasa a Macedonia y ayúdanos. 
                    10 Cuando vio la visión, en seguida procuramos partir para Macedonia, 
                    dando por cierto que Dios nos llamaba para que les anunciásemos el evangelio.
                </em></p>
            <p>También podríamos tener una mala interpretación cuando el Señor Jesús le dijo a 
                la mujer Cananea que él fue enviado a las ovejas perdidas de Israel y que no 
                podía echarles el pan a los perros, esto lo puede leer en <em>Mateo 15:24 
                    El respondiendo, dijo: No soy enviado sino a las ovejas perdidas de la 
                    casa de Israel. 25 Entonces ella vino y se postró ante él, diciendo: 
                    ¡Señor, socórreme! 26 Respondiendo él, dijo: No está bien tomar el pan 
                    de los hijos, y echarlo a los perrillos.</em></p>
            <p>Son palabras muy duras, al corazón de una mujer cananea profundamente necesitad. 
                Pero El Señor en su ministerio terrenal tenía un objetivo, para que el plan de 
                Salvación para la humanidad pudiera cumplirse, también muestra la fidelidad de 
                Dios con un pueblo que sin merecerlo fue escogido para que el Mesías se hiciera 
                hombre, leemos en <em>Juan 1:11 A lo suyo vino, y los suyos no le recibieron. 
                    12 Mas a todos los que le recibieron.</em></p>
